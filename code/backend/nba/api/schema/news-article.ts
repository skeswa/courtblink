/** Represents an NBA news article. */
export type NewsArticle = {
  /** Name of the person who wrote it. */
  author?: string
  /** Description of the article itself. */
  description?: string
  /** URL to the image that accompanies this article. */
  imageUrl?: string
  /** Link to this article. */
  link?: string
  /** Title of this article. */
  title?: string
}
